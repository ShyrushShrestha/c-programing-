#include<stdio.h>

int main ()
{
	char a=65;
	
	while (a<=90)
	{
	printf("\t%c",a);	
	a++;
	}
	printf("\n \n");
	a=97;
	while (a<=122)
	{
	printf("\t%c",a);
	a++;	
	}
	
	return 0;

}
